import statsmodels.api as sm
import matplotlib.pyplot as plt

print("--- PREPARANDO DADOS PARA A REGRESSÃO OLS (MENSAL) ---")

# 1. Transformando a base diária em mensal para capturar o "Efeito Patamar"
# Certifique-se de que a coluna 'DT_COMPTC' está no formato datetime
df_modelo['DT_COMPTC'] = pd.to_datetime(df_modelo['DT_COMPTC'])
# Criamos uma coluna de Ano-Mês
df_modelo['ANO_MES'] = df_modelo['DT_COMPTC'].dt.to_period('M')

# Agrupamos: Somamos a captação do mês e pegamos a média da Selic naquele mês
df_mensal = df_modelo.groupby('ANO_MES').agg(
    CAPTACAO_MENSAL=('CAPTACAO_LIQUIDA_TOTAL', 'sum'),
    SELIC_MEDIA=('SELIC_ANUALIZADA', 'mean')
).reset_index()

# Para facilitar a leitura dos bilhões na tabela do OLS
df_mensal['CAPTACAO_BILHOES'] = df_mensal['CAPTACAO_MENSAL'] / 1e9

print(f"Base convertida de {len(df_modelo)} dias para {len(df_mensal)} meses.\n")

# 2. Configurando o Modelo OLS
# X = Variável Independente (Selic Media) | Y = Variável Dependente (Captação em Bilhões)
X = df_mensal['SELIC_MEDIA']
Y = df_mensal['CAPTACAO_BILHOES']

# O statsmodels exige que adicionemos a constante (o intercepto, Beta 0) manualmente
X_com_constante = sm.add_constant(X)

# 3. Treinando e imprimindo o modelo
modelo_ols = sm.OLS(Y, X_com_constante).fit()
print("--- RESULTADO DA REGRESSÃO (OLS) ---")
print(modelo_ols.summary())

# 4. (Opcional) Visualização Rápida da Linha de Tendência Linear
plt.figure(figsize=(10, 6))
plt.scatter(X, Y, color='#2c3e50', alpha=0.6, label='Meses (Observações)')
plt.plot(X, modelo_ols.predict(X_com_constante), color='#c0392b', linewidth=2, label='Linha de Regressão (OLS)')
plt.axhline(0, color='black', linestyle='--', linewidth=1)
plt.title('Dispersão: Patamar da Selic vs. Fuga de Capital Mensal', fontsize=14, fontweight='bold')
plt.xlabel('Taxa Selic Média do Mês (%)', fontsize=12)
plt.ylabel('Captação Líquida Mensal (Bilhões de R$)', fontsize=12)
plt.legend()
plt.grid(alpha=0.3)
plt.show()

