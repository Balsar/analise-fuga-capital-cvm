def testar_estacionariedade(serie, nome_variavel):
    """
    Aplica o teste ADF para verificar a presença de raiz unitária.
    H0 (Hipótese Nula): A série possui raiz unitária (NÃO é estacionária).
    H1 (Hipótese Alternativa): A série NÃO possui raiz unitária (É estacionária).
    """
    print(f"\n--- Teste Dickey-Fuller Aumentado: {nome_variavel} ---")

    # O adfuller não aceita valores nulos, então garantimos a limpeza com dropna()
    resultado = adfuller(serie.dropna())

    p_value = resultado[1]
    estatistica_adf = resultado[0]

    print(f"Estatística ADF: {estatistica_adf:.4f}")
    print(f"P-value: {p_value:.4f}")

    # Nível de significância estatística rigoroso de 5%
    if p_value < 0.05:
        print("-> CONCLUSÃO: A série é ESTACIONÁRIA (I(0)). O modelo VAR a aceita como está.")
    else:
        print("-> CONCLUSÃO: A série NÃO É ESTACIONÁRIA. Precisaremos aplicar a Primeira Diferença (Δ).")

# 2. Executando o teste para a variável dependente (Y) e independente (X)
